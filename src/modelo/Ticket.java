package modelo;

/**
 *
 * @author Angel
 */
public class Ticket extends Cliente{
    
    String fecha;
    String hora;

    public Ticket() {
    }

    public Ticket(String fecha, String hora) {
        this.fecha = fecha;
        this.hora = hora;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }
    
    
    
}
